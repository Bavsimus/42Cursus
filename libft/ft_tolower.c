#include "libft.h"

int	ft_tolower(int c)
{
	if (c >= 'A' && c <= 'Z')
		return (c + 32);
	return (c);
}

//checks if the character received as an arg is an uppercase
//if it is converts it to lowercase 
//otherwise returns without change
